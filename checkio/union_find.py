def union_find(lis):
    lis = map(set, lis)
    unions = []
    for item in lis:
        temp = []
        for s in unions:
            if not s.isdisjoint(item):
                item = s.union(item)
            else:
                temp.append(s)
        temp.append(item)
        unions = temp
    return unions

def prepare_array(lis):
    connections=[]
    for el in lis:
        first, second = el.split('-')
        connections.append([first,second])
    return connections

def check_connection(network, first, second):
    union_sets = union_find(prepare_array(network))
    #print(union_sets)
    for s in union_sets:
        if first in s and second in s:
            return True
    return False


